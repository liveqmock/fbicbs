package cbs.batch.ac.acbint;

public class ApcGlc {
    private String glcbal;
    private String glcopn;
    private String apctyp;

    public String getGlcbal() {
        return glcbal;
    }

    public void setGlcbal(String glcbal) {
        this.glcbal = glcbal;
    }

    public String getGlcopn() {
        return glcopn;
    }

    public void setGlcopn(String glcopn) {
        this.glcopn = glcopn;
    }

    public String getApctyp() {
        return apctyp;
    }

    public void setApctyp(String apctyp) {
        this.apctyp = apctyp;
    }
}
